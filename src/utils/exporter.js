// Utilities to produce sample frames and convert frames to Arduino C arrays
import { mirrorX, mirrorY, rotateZ90 } from './drawHelpers.js';

export function framesToCArray(frames, name) {
  if (!frames || frames.length === 0) return '// no frames';

  // Apply mirroring transformations to match physical cube orientation
  const transformedFrames = frames.map((frame) => mirrorX(frame));

  const frameCount = transformedFrames.length;
  const cols = 64;
  let out = `// Generated by LED Cube Designer\n#define ${name}_FRAME_COUNT ${frameCount}\n\nconst byte ${name}[${frameCount}][${cols}] = {\n`;

  transformedFrames.forEach((f, fi) => {
    out += `  // Frame ${fi}\n  {`;
    for (let i = 0; i < cols; i++) {
      const v = f[i] || 0;
      out += '0x' + v.toString(16).toUpperCase().padStart(2, '0');
      if (i < cols - 1) out += ', ';
      if ((i + 1) % 8 === 0 && i < cols - 1) out += '\n   ';
    }
    out += '}';
    if (fi < frameCount - 1) out += ',\n\n';
    else out += '\n';
  });

  out += '};\n';
  return out;
}

export function generateHFile(name, frames) {
  const arr = framesToCArray(frames, name);
  const guard = `_${name}_H_`.toUpperCase();
  let out =
    `#ifndef ${guard}\n#define ${guard}\n\n#include <Arduino.h>\n\n` +
    arr +
    `\n#endif // ${guard}\n`;
  return out;
}

export function generateSketch(name, frames) {
  // generate the C array text first
  let arr = framesToCArray(frames, name);

  // replace the array declaration to place it in flash (PROGMEM)
  const header = `const byte ${name}[${frames.length}][64] = {`;
  const headerProg = `const byte ${name}[${frames.length}][64] PROGMEM = {`;
  arr = arr.replace(header, headerProg);

  // sketch template using PROGMEM and pgm_read_byte
  const sketch = `// Generated Arduino sketch for ${name}\n\n#include <Arduino.h>\n#include <avr/pgmspace.h>\n\n${arr}\n\n// send one frame stored in PROGMEM (framePtr points to 64 bytes)\nvoid sendFrameFromPROGMEM(const uint8_t *framePtr) {\n  Serial.write(0xF2);\n  for (int i = 0; i < 64; i++) {\n    uint8_t b = pgm_read_byte(framePtr + i);\n    Serial.write(b);\n  }\n}\n\nvoid setup(){\n  Serial.begin(38400);\n  delay(500);\n  // send open command\n  for(int i=0;i<70;i++) Serial.write(0xAD);\n  delay(200);\n}\n\nvoid loop(){\n  for (int f = 0; f < ${name}_FRAME_COUNT; f++) {\n    const uint8_t *framePtr = ${name}[f];\n    sendFrameFromPROGMEM(framePtr);\n    delay(200);\n  }\n  // keep looping\n}\n`;

  return sketch;
}

// This board's cube has its own onboard controller that does the real LED
// driving in its own firmware -- it isn't wired to Arduino GPIO pins at all.
// It speaks a fixed protocol (0xAD open handshake, then 0xF2 + 64 raw bytes
// per frame, repeated forever) directly over the same serial line the
// Arduino uses to talk to your PC. The exported ANIM.ino confirms this: its
// loop() only ever calls Serial.write() -- it never touches a pin.
//
// That means the Arduino's job here is just to relay bytes from USB
// straight through to the cube controller, verbatim, in real time. No
// parsing, no checksum, no ACK/NACK -- the previous version of this sketch
// implemented a request/confirm protocol the cube controller was never
// going to speak, which is why it silently did nothing no matter what was
// sent to it.
export function generateStreamingReceiverSketch() {
  const sketch = `// Live relay sketch for LED Cube
//
// This board's cube has its own onboard controller that does the actual
// LED driving -- this sketch does not touch any pins for LEDs. It just
// forwards every byte it receives over USB straight out its hardware
// Serial line to that controller, unmodified, in real time.
//
// Flash this and leave the board connected. On the website, click Connect,
// then use the Playback bar's Stream controls (not a one-shot Send) --
// this cube's controller falls back to its own built-in demo pattern
// whenever the live feed stops, so the site streams frames in a
// continuous loop for as long as you leave streaming on, the same way the
// exported ANIM.ino does.

#include <Arduino.h>

void setup() {
  Serial.begin(38400);
}

void loop() {
  if (Serial.available() > 0) {
    Serial.write(Serial.read());
  }
}
`;

  return sketch;
}

export function generateESP32Sketch(name, frames) {
  let arr = framesToCArray(frames, name);
  const header = `const byte ${name}[${frames.length}][64] = {`;
  const headerProg = `const byte ${name}[${frames.length}][64] PROGMEM = {`;
  arr = arr.replace(header, headerProg);

  return `// Generated ESP32 sketch for ${name}
// Flashes animation into ESP32 flash memory (PROGMEM)

#include <Arduino.h>
#include <pgmspace.h>

${arr}

// Send one frame stored in PROGMEM (framePtr points to 64 bytes)
void sendFrameFromPROGMEM(const uint8_t *framePtr) {
  Serial.write(0xF2);
  for (int i = 0; i < 64; i++) {
    uint8_t b = pgm_read_byte(framePtr + i);
    Serial.write(b);
  }
}

void setup() {
  // Initialize Hardware Serial for USB / Cube connection at 38400 baud
  Serial.begin(38400);
  delay(500);
  // Send open command handshake (0xAD)
  for (int i = 0; i < 70; i++) {
    Serial.write(0xAD);
  }
  delay(200);
}

void loop() {
  for (int f = 0; f < ${name}_FRAME_COUNT; f++) {
    const uint8_t *framePtr = ${name}[f];
    sendFrameFromPROGMEM(framePtr);
    delay(200);
  }
}
`;
}

